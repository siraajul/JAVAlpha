package Loops;

import java.util.Scanner;

public class whileloop {
    public static void main(String[] args) {

        //Scanner input = new Scanner(System.in);
        int counter = 0;

        while(counter<100){
            System.out.println("Hello Java");
            counter++;
        }
        System.out.println("Printed 100x Times");
    }
}
